package day18.condition;

public class switchcase3 {

	public static void main(String[] args) {
		int value =4;
		switch (value)  {
		case 1:
			System.out.println("Hi,today is monday");
		break;
		case 2:
			System.out.println("Hi,today is tuesdy");
		break;
		case 3:
			System.out.println("Hi,today is wednesday");
		break;
		case 4:
			System.out.println("Hi,today is thursday");
		break;
		case 5:
			System.out.println("Hi,today is monday");
		break;
		case 6:
			System.out.println("Hi,today is monday");
		break;
		
		default:
			System.out.println("none of your cases matched");
			break;
		}

	}

}

