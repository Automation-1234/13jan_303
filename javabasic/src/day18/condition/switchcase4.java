package day18.condition;

public class switchcase4 {

	public static void main(String[] args) {
		int month =1;
		switch (month) {
		case 1:
			System.out.println("month is january");
			//break;
			
		case 2:
			System.out.println("month is february");
			break;
			
			default:
				System.out.println("not match");
				break;
		}

	}

}
